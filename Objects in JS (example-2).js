// Objects in JS (example-2)

// Creating a Object
Laptop = {
    // Declaring the object variables 
    name: "Dell XPS 40",
    Processor: "I5 8th Gen"
}

// Printing the specific properties in an object
console.log(Laptop.name); // with help of (.) operator
                 // OR
console.log(Laptop['Processor']); // using the ([]) operator // [here we can use both both "" and '']
