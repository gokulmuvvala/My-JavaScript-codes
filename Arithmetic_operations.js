/* Arithmetic operations in Java script
Here we are considering (a) and (b) are variables */

// Creating the a & b variables
var a = 24
var b = 16

// Addition
console.log(a+b); // 40

// Difference between a & b or subtraction
console.log(a-b); // 8

// Product
console.log(a*b); // 384

// division
console.log(a/b); // 1.5

// Remainder between a and b (or) Modulo
console.log(a%b); // 8

